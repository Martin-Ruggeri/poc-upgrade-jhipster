    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Note createEntity(EntityManager em) {
        Note note = new Note().idUser(DEFAULT_ID_USER).description(DEFAULT_DESCRIPTION).date(DEFAULT_DATE);
        // Add required entity
        Well well;
        if (TestUtil.findAll(em, Well.class).isEmpty()) {
            well = WellResourceIT.createEntity(em);
            em.persist(well);
            em.flush();
        } else {
            well = TestUtil.findAll(em, Well.class).get(0);
        }
        note.setWell(well);
        return note;
    }

    /**
     * Create an updated entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
